package com.skcc.cloudz.zcp.alertmanager.common.vo;

public class RuleData {

	private String ruleName;
	private String ruleAlert;
	private String ruleExpr;
	private String ruleFor;
	private String ruleSeverity;
	private String ruleChannel;
	private String ruleDescription;
	private String ruleSummary;

	/**
	 * @return the ruleName
	 */
	public String getRuleName() {
		return ruleName;
	}

	/**
	 * @param ruleName
	 *            the ruleName to set
	 */
	public void setRuleName(String ruleName) {
		this.ruleName = ruleName;
	}

	/**
	 * @return the ruleAlert
	 */
	public String getRuleAlert() {
		return ruleAlert;
	}

	/**
	 * @param ruleAlert
	 *            the ruleAlert to set
	 */
	public void setRuleAlert(String ruleAlert) {
		this.ruleAlert = ruleAlert;
	}

	/**
	 * @return the ruleExpr
	 */
	public String getRuleExpr() {
		return ruleExpr;
	}

	/**
	 * @param ruleExpr
	 *            the ruleExpr to set
	 */
	public void setRuleExpr(String ruleExpr) {
		this.ruleExpr = ruleExpr;
	}

	/**
	 * @return the ruleFor
	 */
	public String getRuleFor() {
		return ruleFor;
	}

	/**
	 * @param ruleFor
	 *            the ruleFor to set
	 */
	public void setRuleFor(String ruleFor) {
		this.ruleFor = ruleFor;
	}

	/**
	 * @return the ruleSeverity
	 */
	public String getRuleSeverity() {
		return ruleSeverity;
	}

	/**
	 * @param ruleSeverity
	 *            the ruleSeverity to set
	 */
	public void setRuleSeverity(String ruleSeverity) {
		this.ruleSeverity = ruleSeverity;
	}

	/**
	 * @return the ruleChannel
	 */
	public String getRuleChannel() {
		return ruleChannel;
	}

	/**
	 * @param ruleChannel
	 *            the ruleChannel to set
	 */
	public void setRuleChannel(String ruleChannel) {
		this.ruleChannel = ruleChannel;
	}

	/**
	 * @return the ruleDescription
	 */
	public String getRuleDescription() {
		return ruleDescription;
	}

	/**
	 * @param ruleDescription
	 *            the ruleDescription to set
	 */
	public void setRuleDescription(String ruleDescription) {
		this.ruleDescription = ruleDescription;
	}

	/**
	 * @return the ruleSummary
	 */
	public String getRuleSummary() {
		return ruleSummary;
	}

	/**
	 * @param ruleSummary
	 *            the ruleSummary to set
	 */
	public void setRuleSummary(String ruleSummary) {
		this.ruleSummary = ruleSummary;
	}

}

